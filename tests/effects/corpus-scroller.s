    stop    #$2300               ; wait for VBL
    bra.s   main

; Draw SCRW columns of the band from strip[srccol..], column by column so
; the strip offset is computed once per column (not per cell) -> the whole
; redraw fits inside vblank (no tear at the wrap). strip is column-major:
; the BANDH words of column k are contiguous at strip + k*BANDH*2.
drawband:
    move.l  screen,a1
    lea     STRIDE*BANDTOP(a1),a1  ; a1 -> band top-left
    move.w  srccol,d4
    move.w  #SCRW-1,d3
.dc:
    move.w  d4,d5                ; strip column (srccol + c)
    mulu    #BANDH*2,d5          ; -> byte offset of the column
    lea     strip,a2
    add.l   d5,a2                ; a2 -> column's first word
    move.l  a1,a0                ; a0 -> band top of this screen column
    move.w  #BANDH-1,d2
.dr:
    move.w  (a2)+,(a0)          ; plane0 glyph bits for this scanline
    clr.w   2(a0)               ; planes 1-3 clear (2-colour)
    clr.w   4(a0)
    clr.w   6(a0)
    lea     STRIDE(a0),a0       ; next scanline
    dbf     d2,.dr
    lea     8(a1),a1            ; next screen column (+16px)
    addq.w  #1,d4
    dbf     d3,.dc
    rts

vbl:
    move.w  #$2700,sr
    movem.l d0-d7/a0-a2,-(sp)
    move.w  fine,d0
    move.b  d0,HSCROLL5          ; STE prefetch fine scroll (0..15)
    add.w   #1,d0               ; advance by speed px
    cmp.w   #16,d0
    blt.s   .keep
    sub.w   #16,d0               ; wrapped a 16px column
    addq.w  #1,srccol            ; advance the source strip one column
    cmp.w   #SRCCOLS-SCRW,srccol
    blt.s   .redraw
    move.w  #0,srccol            ; loop the message
.redraw:
    bsr     drawband             ; hand off: band shifted one column
.keep:
    move.w  d0,fine
    movem.l (sp)+,d0-d7/a0-a2
    rte

