    move.l  d0,screen
    move.l  d0,$44e.w            ; logbase -> our buffer (drawband uses $44e)
    move.b  d0,VIDBASEL          ; program the shifter base (fixed all run)
    lsr.l   #8,d0
    move.b  d0,VIDBASEM
    lsr.l   #8,d0
    move.b  d0,VIDBASEH
