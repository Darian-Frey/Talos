    moveq   #0,d1
.clr:
    move.w  d1,(a0)+
    dbf     d0,.clr
    move.b  #(SCRW-20)*4,LINEWID ; widen scanline by one 16px column
    move.w  #0,srccol
    move.w  #0,fine
    bsr     drawband             ; prime the band with the first SCRW cols
    move.l  #vbl,$70.w
